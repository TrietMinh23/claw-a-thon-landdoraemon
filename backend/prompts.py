def build_system_prompt(
    email_type: str,
    program_name: str = "",
    purpose: str = "",
    datetime: str = "",
    location: str = "",
    description: str = "",
) -> str:
    sample = MAIL_SAMPLES.get(email_type, "")
    sample_section = f"\nVí dụ email thực tế:\n---\n{sample}\n---\n" if sample else ""

    fields = []
    if program_name:
        fields.append(f"- Tên chương trình: {program_name}")
    if purpose:
        fields.append(f"- Mục đích: {purpose}")
    if datetime:
        fields.append(f"- Thời gian: {datetime}")
    if location:
        fields.append(f"- Địa điểm: {location}")
    if description:
        fields.append(f"- Mô tả nội dung: {description}")

    fields_section = "\n".join(fields)

    return f"""{_TORO_PERSONA}
{sample_section}
Hãy soạn một email mới dựa trên thông tin sau:
{fields_section}

Viết email theo cấu trúc và giọng văn tương tự ví dụ trên. Chỉ trả về HTML email, không giải thích thêm."""


