def build_html_email_prompt(
    email_type: str,
    workshop_context: str = "",
    extra_instructions: str = "",
    image_urls: list[str] | None = None,
) -> str:
    tone = _TONE_HINTS.get(email_type, "")
    _sample_key = "setup_afit" if email_type == "setup" else email_type
    sample = MAIL_SAMPLES.get(_sample_key, "")
    sample_section = f"\nVí dụ email thực tế (dùng làm tham khảo giọng văn):\n---\n{sample}\n---\n" if sample else ""

    context_section = f"\nThông tin workshop / chương trình:\n{workshop_context}" if workshop_context else ""
    extra_section = f"\nYêu cầu bổ sung:\n{extra_instructions}" if extra_instructions else ""

    image_section = ""
    if image_urls:
        slots = "\n".join(f"  IMAGE_SLOT_{i} = {url}" for i, url in enumerate(image_urls))
        image_section = f"\nẢnh đính kèm — dùng URL sau làm src trực tiếp trong thẻ <img>:\n{slots}\n"

    return f"""{_TORO_HTML_PERSONA}

Loại email: {email_type}. {tone}
{sample_section}{context_section}{extra_section}{image_section}
Soạn email theo cấu trúc HTML ở trên. Chỉ trả về HTML."""
